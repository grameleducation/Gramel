import fs from "fs/promises";
import path from "path";
import { Pool } from "pg";

const TABLES = ["users", "account", "session", "verification"];
const DEFAULT_OUTPUT = path.resolve(process.cwd(), "auth-tables-export.sql");

function quoteIdentifier(identifier) {
  return `"${String(identifier).replace(/"/g, '""')}"`;
}

function quoteLiteral(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

function normalizeBoolean(value) {
  if (typeof value === "boolean") return value;
  if (value === "t" || value === "true") return true;
  if (value === "f" || value === "false") return false;
  return Boolean(value);
}

function formatValue(value, dataType) {
  if (value === null || value === undefined) return "NULL";

  if (dataType === "json" || dataType === "jsonb") {
    return `${quoteLiteral(JSON.stringify(value))}::${dataType}`;
  }

  if (dataType === "boolean") {
    return normalizeBoolean(value) ? "TRUE" : "FALSE";
  }

  if (
    dataType === "smallint" ||
    dataType === "integer" ||
    dataType === "bigint" ||
    dataType === "real" ||
    dataType === "double precision" ||
    dataType === "numeric"
  ) {
    return String(value);
  }

  if (value instanceof Date) {
    return quoteLiteral(value.toISOString());
  }

  return quoteLiteral(value);
}

async function getTableColumns(pool, tableName) {
  const { rows } = await pool.query(
    `SELECT column_name, data_type, ordinal_position
     FROM information_schema.columns
     WHERE table_schema = 'public' AND table_name = $1
     ORDER BY ordinal_position ASC`,
    [tableName],
  );

  if (rows.length === 0) {
    throw new Error(`Table public.${tableName} was not found.`);
  }

  return rows;
}

async function getTableRows(pool, tableName, columns) {
  const orderedColumns = columns.map((column) =>
    quoteIdentifier(column.column_name),
  );
  const orderByColumn = "created_at";
  // const orderByColumn = columns[0]?.column_name ?? "id";

  const query = `SELECT ${orderedColumns.join(", ")} FROM public.${quoteIdentifier(tableName)} ORDER BY ${quoteIdentifier(orderByColumn)} ASC`;
  const { rows } = await pool.query(query);
  return rows;
}

function buildInsertStatements(tableName, columns, rows) {
  const columnNames = columns
    .map((column) => quoteIdentifier(column.column_name))
    .join(", ");

  if (rows.length === 0) {
    return [`-- public.${tableName}: no rows`];
  }

  return rows.map((row) => {
    const values = columns
      .map((column) => formatValue(row[column.column_name], column.data_type))
      .join(", ");

    return `INSERT INTO public.${quoteIdentifier(tableName)} (${columnNames}) VALUES (${values});`;
  });
}

async function main() {
  const connectionString = process.env.DATABASE_URL || process.env.POSTGRES_URL;

  if (!connectionString) {
    throw new Error(
      "Missing database connection string. Set DATABASE_URL, or POSTGRES_URL.",
    );
  }

  const outputPath = process.argv[2]
    ? path.resolve(process.cwd(), process.argv[2])
    : DEFAULT_OUTPUT;

  const pool = new Pool({ connectionString });

  try {
    const lines = [
      "-- Export generated by scripts/export-auth-tables-to-sql.js",
      `-- Generated at ${new Date().toISOString()}`,
      "BEGIN;",
      "",
    ];

    for (const tableName of TABLES) {
      const columns = await getTableColumns(pool, tableName);
      const rows = await getTableRows(pool, tableName, columns);
      const statements = buildInsertStatements(tableName, columns, rows);

      lines.push(
        `-- public.${tableName} (${rows.length} row${rows.length === 1 ? "" : "s"})`,
      );
      lines.push(...statements);
      lines.push("");
    }

    lines.push("COMMIT;", "");

    await fs.writeFile(outputPath, lines.join("\n"), "utf8");

    console.log(`Wrote SQL export to ${outputPath}`);
  } finally {
    await pool.end();
  }
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
